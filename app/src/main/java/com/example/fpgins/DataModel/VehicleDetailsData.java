package com.example.fpgins.DataModel;

public class VehicleDetailsData {
    private String mMake;
    private String mModel;
    private String mVariant;
    private String mTypeBody;
    private String mYearManufactured;
    private String mPlateNumber;
    private String mConductionSticker;
    private String mMvFileNo;
    private String mEngineNo;
    private String mChassisNo;
    private String mColor;
    private String mPassengerNo;
    private String mAccessories;

    public VehicleDetailsData(String mMake, String mModel, String mVariant, String mTypeBody,
                              String mYearManufactured, String mPlateNumber, String mConductionSticker, String mMvFileNo, String mEngineNo, String mChassisNo, String mColor, String mPassengerNo, String mAccessories) {
        this.mMake = mMake;
        this.mModel = mModel;
        this.mVariant = mVariant;
        this.mTypeBody = mTypeBody;
        this.mYearManufactured = mYearManufactured;
        this.mPlateNumber = mPlateNumber;
        this.mConductionSticker = mConductionSticker;
        this.mMvFileNo = mMvFileNo;
        this.mEngineNo = mEngineNo;
        this.mChassisNo = mChassisNo;
        this.mColor = mColor;
        this.mPassengerNo = mPassengerNo;
        this.mAccessories = mAccessories;
    }

    public String getmMake() {
        return mMake;
    }

    public void setmMake(String mMake) {
        this.mMake = mMake;
    }

    public String getmModel() {
        return mModel;
    }

    public void setmModel(String mModel) {
        this.mModel = mModel;
    }

    public String getmVariant() {
        return mVariant;
    }

    public void setmVariant(String mVariant) {
        this.mVariant = mVariant;
    }

    public String getmTypeBody() {
        return mTypeBody;
    }

    public void setmTypeBody(String mTypeBody) {
        this.mTypeBody = mTypeBody;
    }

    public String getmYearManufactured() {
        return mYearManufactured;
    }

    public void setmYearManufactured(String mYearManufactured) {
        this.mYearManufactured = mYearManufactured;
    }

    public String getmPlateNumber() {
        return mPlateNumber;
    }

    public void setmPlateNumber(String mPlateNumber) {
        this.mPlateNumber = mPlateNumber;
    }

    public String getmConductionSticker() {
        return mConductionSticker;
    }

    public void setmConductionSticker(String mConductionSticker) {
        this.mConductionSticker = mConductionSticker;
    }

    public String getmMvFileNo() {
        return mMvFileNo;
    }

    public void setmMvFileNo(String mMvFileNo) {
        this.mMvFileNo = mMvFileNo;
    }

    public String getmEngineNo() {
        return mEngineNo;
    }

    public void setmEngineNo(String mEngineNo) {
        this.mEngineNo = mEngineNo;
    }

    public String getmChassisNo() {
        return mChassisNo;
    }

    public void setmChassisNo(String mChassisNo) {
        this.mChassisNo = mChassisNo;
    }

    public String getmColor() {
        return mColor;
    }

    public void setmColor(String mColor) {
        this.mColor = mColor;
    }

    public String getmPassengerNo() {
        return mPassengerNo;
    }

    public void setmPassengerNo(String mPassengerNo) {
        this.mPassengerNo = mPassengerNo;
    }

    public String getmAccessories() {
        return mAccessories;
    }

    public void setmAccessories(String mAccessories) {
        this.mAccessories = mAccessories;
    }
}
